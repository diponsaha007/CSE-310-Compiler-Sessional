	POP DX
	POP DX
	JMP LABEL2
LABEL3:
	POP DX
	PUSH 4
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+2] , AX
	PUSH 6
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+0] , AX
;code while (k>0)
LABEL6:
	PUSH 0
