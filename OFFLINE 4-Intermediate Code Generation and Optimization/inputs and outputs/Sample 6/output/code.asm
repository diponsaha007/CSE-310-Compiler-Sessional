	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+2] , AX
	PUSH 6
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+0] , AX
;code while (k--)
LABEL8:
;Incrementing varibale k, -1
