	JMP LABEL5


LABEL4:
	MOV BX,1


LABEL5:
	POP DX
	PUSH BX
	MOV BP,SP
	MOV AX , [BP+0]
	CMP AX , 0
	JE LABEL7
	PUSH 3
	MOV BP,SP
	MOV AX, [BP+4]
	ADD AX, [BP+0]
	POP DX
	PUSH AX
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+2] , AX
;Incrementing varibale k, -1
	MOV BP,SP
	MOV AX,[BP+4]
	PUSH AX
	ADD AX,-1
	MOV [BP+4] , AX
	POP DX
	POP DX
	JMP LABEL6
LABEL7:
	POP DX
;printing l
	MOV BP,SP
	MOV AX, [BP+ 0]
	CALL PRINTLN
;printing k
	MOV BP,SP
	MOV AX, [BP+ 2]
	CALL PRINTLN
	PUSH 4
