	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+2] , AX
	POP DX
	JMP LABEL8
LABEL9:
	POP DX
;printing l
	MOV BP,SP
	MOV AX, [BP+ 0]
	CALL PRINTLN
;printing k
	MOV BP,SP
	MOV AX, [BP+ 2]
