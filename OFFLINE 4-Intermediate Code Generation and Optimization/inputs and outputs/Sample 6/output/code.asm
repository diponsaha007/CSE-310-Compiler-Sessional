.MODEL SMALL

.STACK 100H

.DATA

	CR EQU 0DH
	LF EQU 0AH

.CODE


PRINTLN PROC

;PRINTS THE NUMBER STORED IN AX
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	CMP AX,0
	JGE HERE
;NEGATIVE NUMBER
	PUSH AX
	MOV AH, 2
	MOV DL, '-'
	INT 21H
	POP AX
	NEG AX
HERE:
	XOR CX,CX
	MOV BX , 10
LOOP_:
	CMP AX,0
	JE END_LOOP
	XOR DX,DX
	DIV BX
	PUSH DX
	INC CX
	JMP LOOP_
END_LOOP:
	CMP CX,0
	JNE PRINTER
	MOV AH,2
	MOV DL,'0'
	INT 21H
	JMP ENDER
PRINTER:
	MOV AH,2
	POP DX
	OR DL,30H
	INT 21H
	LOOP PRINTER
ENDER:
;PRINT NEW LINE
	MOV AH, 2
	MOV DL , LF
	INT 21H
	MOV DL , CR
	INT 21H
	POP DX
	POP CX
	POP BX
	POP AX
	RET
PRINTLN ENDP

MAIN PROC

	MOV AX, @DATA
	MOV DS, AX
	PUSH 0
	PUSH 0
	PUSH 0
	PUSH 0
;code for (i=0; i<6; i++)
	PUSH 0
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+6] , AX
LABEL2:
	PUSH 6
; doing relop operation i<6
	MOV BP,SP
	MOV AX, [BP+8]
	CMP AX , [BP+0]
