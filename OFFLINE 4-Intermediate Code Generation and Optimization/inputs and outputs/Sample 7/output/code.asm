	MOV BP,SP
	MOV AX, [BP+2]
	ADD AX, [BP+0]
	POP DX
	POP DX
	PUSH AX
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+0] , AX
;Return c
	MOV BP,SP
	MOV AX,[BP+0]
	POP DX
	POP DX
	POP DX
	PUSH DI
	RET
	PUSH DI
	RET
bar ENDP

foo PROC

	POP DI
	PUSH 3
	MOV BP,SP
	MOV AX, [BP+2]
	ADD AX, [BP+0]
	POP DX
	PUSH AX
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+0] , AX
;Return a
	MOV BP,SP
	MOV AX,[BP+0]
	POP DX
	PUSH DI
	RET
	PUSH DI
	RET
foo ENDP

MAIN PROC

	MOV AX, @DATA
	MOV DS, AX
	PUSH 0
	PUSH 0
	PUSH 0
	PUSH 0
	PUSH 5
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+6] , AX
	PUSH 6
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+4] , AX
;calling function foo
	PUSH DI
	MOV BP,SP
	PUSH [BP+8]
	CALL foo
	POP DI
	PUSH AX
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+2] , AX
;printing k
	MOV BP,SP
	MOV AX, [BP+ 2]
	CALL PRINTLN
;calling function bar
	PUSH DI
	MOV BP,SP
	PUSH [BP+8]
	MOV BP,SP
	PUSH [BP+8]
	CALL bar
	POP DI
	PUSH AX
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+0] , AX
;printing l
	MOV BP,SP
	MOV AX, [BP+ 0]
	CALL PRINTLN
	PUSH 6
;calling function bar
	PUSH DI
	MOV BP,SP
	PUSH [BP+10]
	MOV BP,SP
	PUSH [BP+10]
	CALL bar
	POP DI
	PUSH AX
; doing mulop operation 6*bar(i,j)
	MOV BP,SP
	MOV AX , [BP+2]
	IMUL [BP+0]
	POP DX
	POP DX
	PUSH AX
	PUSH 2
	MOV BP,SP
	MOV AX, [BP+2]
	ADD AX, [BP+0]
	POP DX
	POP DX
	PUSH AX
	PUSH 3
;calling function foo
	PUSH DI
	MOV BP,SP
	PUSH [BP+12]
	CALL foo
	POP DI
	PUSH AX
; doing mulop operation 3*foo(i)
	MOV BP,SP
	MOV AX , [BP+2]
	IMUL [BP+0]
	POP DX
