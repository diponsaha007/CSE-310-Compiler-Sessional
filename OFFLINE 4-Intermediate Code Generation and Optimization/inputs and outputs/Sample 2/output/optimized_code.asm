.MODEL SMALL
.STACK 100H
.DATA
	CR EQU 0DH
	LF EQU 0AH
.CODE
PRINTLN PROC
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	CMP AX,0
	JGE HERE
	PUSH AX
	MOV AH, 2
	MOV DL, '-'
	INT 21H
	POP AX
	NEG AX
HERE:
	XOR CX,CX
	MOV BX , 10
LOOP_:
	CMP AX,0
	JE END_LOOP
	XOR DX,DX
	DIV BX
	PUSH DX
	INC CX
	JMP LOOP_
END_LOOP:
	CMP CX,0
	JNE PRINTER
	MOV AH,2
	MOV DL,'0'
	INT 21H
	JMP ENDER
PRINTER:
	MOV AH,2
	POP DX
	OR DL,30H
	INT 21H
	LOOP PRINTER
ENDER:
	MOV AH, 2
	MOV DL , LF
	INT 21H
	MOV DL , CR
	INT 21H
	POP DX
	POP CX
	POP BX
	POP AX
	RET
PRINTLN ENDP
f PROC
	POP DI
	PUSH 2
	MOV BP,SP
	MOV AX , [BP+0]
	IMUL [BP+2]
	POP DX
	POP DX
	PUSH DI
	RET
	MOV AX,9
	MOV BP,SP
	MOV [BP+0] , AX
	PUSH DI
	RET
f ENDP
g PROC
	POP DI
	PUSH 0
	PUSH DI
	MOV BP,SP
	PUSH [BP+6]
	CALL f
	POP DI
	PUSH AX
	MOV BP,SP
	MOV AX, [BP+0]
	ADD AX, [BP+6]
	POP DX
	PUSH AX
	MOV BP,SP
	MOV AX, [BP+0]
	ADD AX, [BP+4]
	POP DX
	MOV BP,SP
	MOV [BP+0] , AX
	MOV BP,SP
	MOV AX,[BP+0]
	POP DX
	POP DX
	POP DX
	PUSH DI
	RET
	PUSH DI
	RET
g ENDP
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	PUSH 0
	PUSH 0
	MOV AX,1
	MOV BP,SP
	MOV [BP+2] , AX
	MOV AX,2
	MOV BP,SP
	MOV [BP+0] , AX
	PUSH DI
	MOV BP,SP
	PUSH [BP+4]
	MOV BP,SP
	PUSH [BP+4]
	CALL g
	POP DI
	MOV BP,SP
	MOV [BP+2] , AX
	MOV BP,SP
	MOV AX, [BP+ 2]
