.MODEL SMALL

.STACK 100H

.DATA

	CR EQU 0DH
	LF EQU 0AH
	TT0 DW 3 DUP(?)

.CODE


PRINTLN PROC

;PRINTS THE NUMBER STORED IN AX
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	CMP AX,0
	JGE HERE
;NEGATIVE NUMBER
	PUSH AX
	MOV AH, 2
	MOV DL, '-'
	INT 21H
	POP AX
	NEG AX
HERE:
	XOR CX,CX
	MOV BX , 10
LOOP_:
	CMP AX,0
	JE END_LOOP
	XOR DX,DX
	DIV BX
	PUSH DX
	INC CX
	JMP LOOP_
END_LOOP:
	CMP CX,0
	JNE PRINTER
	MOV AH,2
	MOV DL,'0'
	INT 21H
	JMP ENDER
PRINTER:
	MOV AH,2
	POP DX
	OR DL,30H
	INT 21H
	LOOP PRINTER
ENDER:
;PRINT NEW LINE
	MOV AH, 2
	MOV DL , LF
	INT 21H
	MOV DL , CR
	INT 21H
	POP DX
	POP CX
	POP BX
	POP AX
	RET
PRINTLN ENDP

MAIN PROC

	MOV AX, @DATA
	MOV DS, AX
	PUSH 0
	PUSH 0
	PUSH 1
	PUSH 2
	PUSH 3
	MOV BP,SP
	MOV AX, [BP+2]
	ADD AX, [BP+0]
	POP DX
	POP DX
	PUSH AX
; doing mulop operation 1*(2+3)
	MOV BP,SP
	MOV AX , [BP+2]
	IMUL [BP+0]
	POP DX
	POP DX
	PUSH AX
	PUSH 3
; doing mulop operation 1*(2+3)%3
	MOV BP,SP
	MOV AX, [BP+2]
	CWD
	MOV BX,[BP+0]
	IDIV BX
	IMUL BX
	MOV BX,[BP+2]
	SUB BX , AX
	POP DX
	POP DX
	PUSH BX
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+2] , AX
	PUSH 1
	PUSH 5
; doing relop operation 1<5
	MOV BP,SP
	MOV AX, [BP+2]
	CMP AX , [BP+0]
	JL LABEL0
	MOV BX,0
	JMP LABEL1


LABEL0:
	MOV BX,1


LABEL1:
	POP DX
	POP DX
	PUSH BX
	MOV BP,SP
	MOV AX , [BP+0]
	POP DX
	MOV BP,SP
	MOV [BP+0] , AX
	PUSH 0
	PUSH 2
	MOV BP,SP
	MOV SI,[BP+2]
	ADD SI,SI
	MOV AX, [BP+0]
	MOV TT0[SI] , AX
	POP DX
	POP DX
; doing logic operation a&&b
	MOV BP,SP
	CMP [BP+2] ,0
	JE LABEL2
	CMP [BP+0] ,0
	JE LABEL2
	MOV BX,1
	JMP LABEL3


LABEL2:
	MOV BX,0


LABEL3:
	PUSH BX
;code if else (a&&b)
	MOV BP,SP
	CMP [BP+ 0] , 0
	JE LABEL4
	PUSH 0
;Incrementing varibale c[0], 1
	MOV BP,SP
	MOV SI , [BP+0]
	ADD SI , SI
	PUSH TT0[SI]
	ADD TT0[SI],1
	POP DX
	POP DX
	JMP LABEL5
LABEL4:
	PUSH 1
	PUSH 0
	MOV BP,SP
	MOV SI , [BP+0]
	ADD SI , SI
	MOV AX , TT0[SI]
	POP DX
	PUSH AX
	MOV BP,SP
	MOV SI,[BP+2]
	ADD SI,SI
	MOV AX, [BP+0]
	MOV TT0[SI] , AX
	POP DX
	POP DX
LABEL5:
	POP DX
;printing a
	MOV BP,SP
	MOV AX, [BP+ 2]
	CALL PRINTLN
;printing b
	MOV BP,SP
	MOV AX, [BP+ 0]
	CALL PRINTLN

;DOS EXIT
	MOV AH, 4CH
	INT 21H
MAIN ENDP
END MAIN

